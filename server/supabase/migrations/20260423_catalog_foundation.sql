-- Base del modulo catalog/imports.
-- Separa el catalogo de competiciones y temporadas del runtime de la liga
-- fantasy, y deja preparada la trazabilidad para futuros imports CSV.

create table if not exists public.platform_user_roles (
    user_id uuid not null,
    role text not null,
    assigned_at timestamptz not null default timezone('utc', now()),
    assigned_by uuid null,
    notes text null,
    constraint platform_user_roles_pkey primary key (user_id, role),
    constraint platform_user_roles_role_check
        check (role in ('catalog_admin'))
);

create index if not exists idx_platform_user_roles_role
    on public.platform_user_roles (role);

create table if not exists public.catalog_competitions (
    id bigint generated by default as identity primary key,
    slug text not null unique,
    provider text not null default 'kaggle',
    source_competition_id integer not null,
    name text not null,
    country text null,
    tier integer null,
    sort_order integer not null default 0,
    is_active boolean not null default true,
    created_at timestamptz not null default timezone('utc', now()),
    updated_at timestamptz not null default timezone('utc', now()),
    constraint catalog_competitions_provider_source_unique
        unique (provider, source_competition_id)
);

create index if not exists idx_catalog_competitions_active_sort
    on public.catalog_competitions (is_active, sort_order, name);

create table if not exists public.catalog_seasons (
    season text primary key,
    sort_order integer not null,
    is_active boolean not null default true,
    created_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_catalog_seasons_active_sort
    on public.catalog_seasons (is_active, sort_order);

create table if not exists public.catalog_dataset_versions (
    id bigint generated by default as identity primary key,
    provider text not null default 'kaggle',
    version_key text not null unique,
    label text not null,
    source_type text not null default 'csv',
    source_filename text null,
    status text not null default 'published',
    created_by uuid null,
    created_at timestamptz not null default timezone('utc', now()),
    published_at timestamptz null,
    constraint catalog_dataset_versions_status_check
        check (status in ('draft', 'validated', 'published', 'archived'))
);

create index if not exists idx_catalog_dataset_versions_status
    on public.catalog_dataset_versions (status);

create table if not exists public.catalog_import_jobs (
    id bigint generated by default as identity primary key,
    dataset_version_id bigint null
        references public.catalog_dataset_versions (id)
        on delete set null,
    job_type text not null,
    status text not null default 'pending',
    filename text null,
    storage_path text null,
    checksum_sha256 text null,
    validation_summary jsonb not null default '{}'::jsonb,
    error_count integer not null default 0,
    created_by uuid null,
    created_at timestamptz not null default timezone('utc', now()),
    started_at timestamptz null,
    finished_at timestamptz null,
    constraint catalog_import_jobs_error_count_check check (error_count >= 0),
    constraint catalog_import_jobs_job_type_check
        check (job_type in ('csv_import', 'catalog_sync')),
    constraint catalog_import_jobs_status_check
        check (status in ('pending', 'validating', 'validated', 'failed', 'published', 'cancelled'))
);

create index if not exists idx_catalog_import_jobs_status_created_at
    on public.catalog_import_jobs (status, created_at desc);

create table if not exists public.catalog_import_job_errors (
    id bigint generated by default as identity primary key,
    job_id bigint not null
        references public.catalog_import_jobs (id)
        on delete cascade,
    row_number integer null,
    field_name text null,
    error_code text not null,
    message text not null,
    raw_payload jsonb null,
    created_at timestamptz not null default timezone('utc', now())
);

create index if not exists idx_catalog_import_job_errors_job_id
    on public.catalog_import_job_errors (job_id);

