alter table public.fantasy_leagues
    add column if not exists competition_id bigint null;

alter table public.fantasy_leagues
    add column if not exists dataset_version_id bigint null;

update public.fantasy_leagues fl
set competition_id = cc.id
from public.catalog_competitions cc
where fl.competition_id is null
  and cc.provider = 'kaggle'
  and cc.source_competition_id = fl.kaggle_league_id;

update public.fantasy_leagues fl
set dataset_version_id = dv.id
from public.catalog_dataset_versions dv
where fl.dataset_version_id is null
  and dv.version_key = 'legacy-bootstrap-v1';

create index if not exists idx_fantasy_leagues_competition_id
    on public.fantasy_leagues (competition_id);

create index if not exists idx_fantasy_leagues_dataset_version_id
    on public.fantasy_leagues (dataset_version_id);

